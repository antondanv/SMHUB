def _stored_file_name_for_spec(spec: MaterialSeedSpec) -> str:
    return f"{uuid5(NAMESPACE_URL, f'smhub:{spec.slug}').hex}.pdf"


def _preview_sidecar_path(file_path: Path) -> Path:
    return file_path.with_suffix(file_path.suffix + ".preview.json")


def _write_preview_sidecar(file_path: Path, spec: MaterialSeedSpec) -> None:
    payload = {
        "title": spec.title,
        "summary": spec.description,
        "sections": [
            {
                "heading": section.heading,
                "bullets": list(section.bullets),
            }
            for section in spec.sections
        ],
        "note": PREVIEW_NOTE,
    }
    _preview_sidecar_path(file_path).write_text(
        json.dumps(payload, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )


def _ensure_material_file(spec: MaterialSeedSpec) -> tuple[Path, str]:
    stored_file_name = _stored_file_name_for_spec(spec)
    file_path = UPLOADS_DIR / stored_file_name
    if not file_path.exists():
        raise RuntimeError(
            "Seed PDF is missing. Restore the pre-generated file at "
            f"'{file_path}' before running app.db.obsidian_materials."
        )
    _write_preview_sidecar(file_path, spec)
    return file_path, str(RELATIVE_UPLOADS_DIR / stored_file_name)


def _get_or_create_editor(session) -> User:
    user = session.scalar(select(User).where(User.email == AUTHOR_EMAIL))
    if user is not None:
        return user

    student_role = session.scalar(select(Role).where(Role.name == UserRole.STUDENT.value))
    course = session.scalar(select(Course).where(Course.number == 2))
    program = session.scalar(select(Program).where(Program.code == "09.03.04"))

    if student_role is None or course is None or program is None:
        raise RuntimeError("Reference data for editorial materials author is missing")

    user = User(
        email=AUTHOR_EMAIL,
        username=AUTHOR_USERNAME,
        hashed_password=hash_password("smhub-editorial-seed"),
        last_name="SMHUB",
        first_name="Редакция",
        middle_name=None,
        role_id=student_role.id,
        is_active=True,
        course_id=course.id,
        program_id=program.id,
        group_name=None,
    )
    session.add(user)
    session.flush()
    return user


def seed_obsidian_materials() -> None:
    with SessionLocal() as session:
        editor = _get_or_create_editor(session)

        subjects = {item.name: item for item in session.scalars(select(Subject)).all()}
        material_types = {item.name: item for item in session.scalars(select(MaterialType)).all()}
        courses = {item.number: item for item in session.scalars(select(Course)).all()}
        programs = {item.code: item for item in session.scalars(select(Program)).all()}
        statuses = {item.name: item for item in session.scalars(select(MaterialStatus)).all()}
        mime_types = {item.name: item for item in session.scalars(select(MimeType)).all()}

        published_status = statuses.get(MaterialStatusEnum.PUBLISHED.value)
        pdf_mime = mime_types.get(PDF_MIME)
        if published_status is None or pdf_mime is None:
            raise RuntimeError("Required material status or mime type is missing")

        existing_materials = session.scalars(
            select(Material).where(Material.author_id == editor.id)
        ).all()
        existing_by_title = {material.title: material for material in existing_materials}
        used_material_ids: set[int] = set()

        for spec in SEED_MATERIALS:
            subject = subjects.get(spec.subject_name)
            material_type = material_types.get(spec.material_type_name)
            course = courses.get(spec.course_number)
            program = programs.get(spec.program_code)
            if subject is None or material_type is None or course is None or program is None:
                raise RuntimeError(f"Reference data missing for material '{spec.title}'")

            file_path, file_url = _ensure_material_file(spec)
            file_size = file_path.stat().st_size

            material = existing_by_title.get(spec.title)
            if material is not None and material.id in used_material_ids:
                material = None
            if material is None:
                material = next(
                    (
                        existing
                        for existing in existing_materials
                        if existing.id not in used_material_ids
                    ),
                    None,
                )

            if material is None:
                material = Material(
                    title=spec.title,
                    description=spec.description,
                    author_id=editor.id,
                    subject_id=subject.id,
                    material_type_id=material_type.id,
                    course_id=course.id,
                    program_id=program.id,
                    mime_type_id=pdf_mime.id,
                    status_id=published_status.id,
                    file_url=file_url,
                    file_name=spec.original_file_name,
                    file_size=file_size,
                    views_count=spec.views_count,
                    downloads_count=spec.downloads_count,
                    likes_count=spec.likes_count,
                    comments_count=0,
                    favorites_count=spec.favorites_count,
                    avg_rating=spec.avg_rating,
                    ratings_count=spec.ratings_count,
                    published_at=spec.published_at,
                )
                session.add(material)
                continue

            used_material_ids.add(material.id)
            material.title = spec.title
            material.description = spec.description
            material.subject_id = subject.id
            material.material_type_id = material_type.id
            material.course_id = course.id
            material.program_id = program.id
            material.mime_type_id = pdf_mime.id
            material.status_id = published_status.id
            material.file_url = file_url
            material.file_name = spec.original_file_name
            material.file_size = file_size
            material.views_count = spec.views_count
            material.downloads_count = spec.downloads_count
            material.likes_count = spec.likes_count
            material.comments_count = 0
            material.favorites_count = spec.favorites_count
            material.avg_rating = spec.avg_rating
            material.ratings_count = spec.ratings_count
            material.published_at = spec.published_at

        session.commit()


if __name__ == "__main__":
    seed_obsidian_materials()
