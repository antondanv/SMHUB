export function getMaterialById(id) {
  return mockMaterials.find((material) => material.id === id) || null;
}

export function getRelatedMaterials(currentMaterialId, subject) {
  return mockMaterials.filter(
    (material) => material.id !== currentMaterialId && material.subject === subject
  );
}
